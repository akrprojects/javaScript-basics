/*
    In programming language like C/C++, when we create an object we have to allocate a memory and deallocate it

    But in Programming language like JavaScript, C#, Java, we don't have this concept

    That is we don't have to explicitly deallocate memory

    The JS engine has a garbage collector meaning it will self-manage itself and find the variables and objects that are no longer in use and it will intelligently free up that memory 

    So, we don't have to worry about allocating and deallocating the memory for our objects

*/